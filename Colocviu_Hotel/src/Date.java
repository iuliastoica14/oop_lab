class Date1 {
    private int zi;
    private int luna;
    private int an;

    Date1(int zi, int luna, int an)
    {
        this.zi=zi;
        this.luna=luna;
        this.an=an;
    }

    public int getZi() {
        return zi;
    }

    public int getLuna() {
        return luna;
    }

    public int getAn() {
        return an;
    }
}
